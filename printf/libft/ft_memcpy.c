#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	char		*d;
	const char	*s;
	size_t		i;

	d = dest;
	s = src;
	i = 0;
	while ((src || dest) && i < n)
	{
		d[i] = s[i];
		i++;
	}
	return (dest);
}
